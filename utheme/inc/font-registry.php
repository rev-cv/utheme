function u_generate_font_scss(): void {
    $registry = u_font_registry();
    $hash     = md5(serialize($registry));

    if (get_option('u_font_registry_hash') === $hash) return;

    $lines = [
        '// Auto-generated from inc/font-registry.php — do not edit manually.',
        '$font-pairs: (',
    ];

    foreach ($registry as $vibe => $data) {
        $lines[] = "    '{$vibe}': (";
        $lines[] = "        'hd-family': '{$data['hd']}',";
        $lines[] = "        'txt-family': '{$data['txt']}',";
        $lines[] = "    ),";
    }

    $lines[] = ');';

    file_put_contents(
        get_template_directory() . '/src/scheme.fonts.scss',
        implode("\n", $lines) . "\n"
    );

    update_option('u_font_registry_hash', $hash);
}
add_action('admin_init', 'u_generate_font_scss');
